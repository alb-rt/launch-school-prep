Methods! 

• Methods allow for an easy way to repeat a common code (or something that you might want to do over and over) without having to type it over and over.

• Methods are defined with the word 'def', then you give it a name
e.g. 

def say
  # method body goes here
end

where say is the name of a method.

Sometimes (or always?) we do something like this:

  def say(words)
    # method body goes here
  end

  where the (words) is a parameter. More on this later when I pickup from the section Default Parameters.

